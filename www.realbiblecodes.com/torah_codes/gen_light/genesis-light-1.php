    <h1>Light of Genesis</h1>
    <p><em>With a beginning, God created 
      heaven and earth. The earth was without form and empty, with darkness 
      on the face of the depths, but God's spirit moved on the water's 
      surface. God said, <q>There shall be light,</q> and lightness became 
      manifest. God saw that the lightness was good and God divided between 
      the lightness and the darkness. God named the lightness Day and the 
      darkness he named night. It was evening and it was morning, one day. 
      (Genesis 1:1-5.)</em> </p>
    <p>The verb describing the action of creation is <span class="hebnorm">ברא</span>, a creation out of nothing. Rabbi 
      Hirsch explains that <span class="hebnorm">ברא</span></p>
    <blockquote>means bringing something into reality which hitherto had 
      only existed inwardly in the mind. It is creating something purely out 
      of one's mind and will and out of nothing else. </blockquote>
    <p>Ibn Ezra provides an additional dimension to this explanation. He 
      says that</p>
    <blockquote>The meaning of <span class="hebnorm">ברא</span> is to cut or 
      set a boundary. The intelligent person will understand what I am 
      alluding to. </blockquote>
    <p>Create, as used in this first verse of Genesis, means that God set a
      boundary, a limit, and contracted himself, His Light, to within this 
      limit thereby making possible the manifestation of something whose 
      consciousness can be directed to independent existence. In this way, 
      that consciousness would not be absorbed and nullified in its Source. </p>
    <p>The first aspect of this contraction creation was heaven and earth: 
      heaven referring to the spiritual dimension of existence, soul, and earth
      referring to the physical dimension of existence, time and space. The 
      earth aspect of creation was without form and empty. This means 
      everything was mixed up and had no purpose. The moment anything is in a 
      way that is not fully connected to God, it is mixed up. It has no Holy 
      purpose. That which is mixed up is dark. God's bringing into existence 
      darkness is what is known in cosmology as the big bang. From this we 
      learn that God created darkness. This darkness is known in the writings 
      of the sages as fire, a primordial fire, (Nachmanides, Rambam) and 
      associated with the Sefirah Gevurah, Strength. As a light it is hidden. </p>
    <p>God's spirit moving refers to the exponential expansion phase 
      immediately after the big bang. Water means in a fluid plasma. Then God 
      gave a purpose, a form, to that darkness which was made to become 
      manifest. There was an awakening. This is what <q>There shall be light</q> accomplished. Existentially, purpose can 
      only exist in a context that contains non-purpose. For purpose is form 
      that arises out of from that which is mixed up. God distinguished between
      that whose function was purpose and that whose function was non-purpose. 
      Purpose was good. God divided between purpose, which is lightness, and 
      that which is mixed up, which is darkness. God named the lightness day 
      and the darkness he named night. The day begins with darkness and ends 
      in lightness. The fabric is One functioning unit. </p>
    <p>Consistent with the Torah's description that the earth aspect of 
      creation was without form and, therefore, dark, we read in Isaiah
    <p>
    <blockquote> <em>[I am the One who] forms lightness and creates darkness. 
      (Isaiah 45:7)</em> </blockquote>
    <p>Isaiah is telling us that the darkness was created and the light was 
      formed. </p>
    <p>This is echoed in Job</p>
    <blockquote> <em>He reveals deep mysteries from the darkness, and brings the deep 
      darkness out into the light. (Job 12:22)</em> </blockquote>
    <p>Rabbeinu Bachya explains that the directive Let there be light means
      that</p>
    <blockquote> <q>the previously hidden light became manifest. The fact that the 
      Torah did not report <q>so it came to be</q> is a hint that the 
      light was not something new, [and therefore formed not created] the 
      only thing new was its visibility.</q> (Torah Commentary by Rabbi Bachya Ben Asher, Vol 1, p 21.) </blockquote>
    <p>This motivates us to explore the key word <em>God creates</em> against
      the key word <em>darkness</em> and the key word <em>God forms</em> against the key word <em>lightness</em>. Using our standard protocol, the
      probability that a text from the ELS random placement text population 
      would produce as compact combined results as we observed from the Torah 
      text for these two pairs is 45.5/1,000. </p>

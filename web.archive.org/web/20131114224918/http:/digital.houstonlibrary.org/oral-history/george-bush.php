<br/>
BB:      _____[21:43]<br/>
<br/>
JM:        July and August but still, I heard it  was unseasonably cool that week the convention was in town.<br/>
<br/>
BB:      It was.   You know, it was funny . . .<br/>
<br/>
GB:      It was cool to me.<br/>
<br/>
BB:      I know it was cool to you, but do you  remember the slogan – I think maybe Jack Steele thought of this – “Houston is  hot.”  That is what the slogan was.  Volunteers cleaned that city.  I mean, what a city.  Even on the routes that the G7 people weren’t  going to take.  But they cleaned the city.<br/>
<br/>
JM:        It is remarkable the loyalty that you  show to Houston, your hometown, and bringing that kind of local attention, and  there was an economic benefit as well.   And it just helped to furnish the city’s reputation.  You moved to Houston in the late 1950s.  Thirty years on, you had seen it grow into a  truly international city.  Talk about the  growth of the city it had experienced.<br/>
<br/>
GB:      Well, it is so firmly writ in the annals  of history but it grew not just on oil and gas but farming, rice farming and many  other . . . petrochemicals, ship channel burgeon.  It was amazing growth and benefitted the  people of Houston.<br/>
<br/>
BB:      ____[23:09].  It was unbelievable the growth.<br/>
<br/>
JM:        When you first moved to Houston, your  office with Zapata Offshore was downtown or was that on the west side?<br/>
<br/>
GB:      The First National Bank Building – the old  First National Bank Building.  And then, later  on, they had one there in what is now the Houston Club Building.<br/>
<br/>
JM:        And then, of course, famously after the  1992 elections, some people speculating you might stay in the East but you did  what you always did – you came back to Houston.<br/>
<br/>
BB:      Isn’t that funny?  People would say, “We don’t think of you as  Houstonians.”  I said, “Well, we have  been there a long, long time and we chose to live there because we love  it.”  Never thought of going anyplace  else.<br/>
<br/>
JM:        Built your house.<br/>
<br/>
BB:      We love our house.<br/>
<br/>
JM:        But you have also helped to build other  things and Celebration of Reading is one of the most popular and successful  events that is looked forward to every year in Houston.<br/>
<br/>
BB:      That is largely volunteers, I might  add.  I take very little credit except  that I was married to the President and the name it was given but people are  unbelievable in helping.  George  certainly has worked at M.D. Anderson and wanted to.<br/>
<br/>
GB:      M.D. Anderson Hospital, they had a little  _____[24:36].<br/>
<br/>
JM:        And you have also helped . . . served on  referendums to build stadiums to help improve the community and whatnot.  What drive do you . . . you could  retire.  You have done your part, you  have given so much to the community and the world but you still turn out for  the Astros games, you still . . .<br/>
<br/>
BB:      We like them!<br/>
<br/>
JM:        You still support so many causes.<br/>
<br/>
GB:      The Astros games are just shear pleasure  and we are grateful to Drayton McLane for getting to sit with him in the  owner’s box there.<br/>
<br/>
BB:      And the Texans.<br/>
<br/>
GB:      And the Texans.<br/>
<br/>
BB:      We love that team.<br/>
<br/>
GB:      Bob McNair.  And we are hoping that that team will make  the playoffs this year and I hope the Astros come alive.  They will.   They have a good lineup, they are just not getting quite the pitching  they need and the hitters are doing pretty well.  But we follow these things.  We like the Rockets.  We have not gone to that many games but I  like what Les Alexander has done for Houston and the Rockets.  Yao-Ming, Battier and some of these others  are just great guys.<br/>
<br/>
BB:      But in answer to your question a little  bit, the reason that we support so many . . . we are retired, but just to go to  a dinner and speak and raise money for something you are very interested in,  that is . . . we are retired.<br/>
<br/>
</p>

<div class="transcript"><a href="george-bush.php#" onmouseover="fixedtooltip('• This transcript has not been corrected and may contain errors. Thank you for your patience until we complete the editorial process.', this, event, '240px')" onmouseout="delayhidetip()">Uncorrected Transcript</a> 
<p><strong>Note:</strong> Original audio recording captured with interview already in progress.</p>

<strong>Interview with:  George W. Bush, Barbara Bush  <br/>
Interviewed by:  Jim McGrath           <br/>
Date:  July 2009 </strong>
<p>&nbsp;</p>
<p><br/>JM:        . . . It was actually in World War II.<br/>
<br/>GB:      World War II.  I was down there, sent down there to try to  get my wings at the Corpus Christi Naval Air Station and did not really get to Houston at that time but  I did go in and out of Corpus itself and really enjoyed it.  So that was the first connection.<br/>
<br/>JM:        Then you served on the <em>San Jacinto</em> during World  War II.  Did you meet any real live  Texans on board the ship?<br/>
<br/>GB:      Well, they flew the Texas flag when we were in combat, and the  captain was a Texan.  I forget his  name.  But that was a good connection  with Texas and, of course, <em>San Jacinto</em> – there is a  great history there.<br/>
<br/>JM:        In the late 1940s, you were a couple on  the move.  You graduated, finished school  at Yale and you were married and had a baby son.<br/>
<br/>GB:      That’s right.<br/>
<br/>JM:        You talked about farming as a vocation  when you were mapping out your plans, didn’t you?  That didn’t pan out?<br/>
<br/>BB:      Well, it turned out that if you wanted to  be a successful farmer, you had to have a little money to buy the farm and to  supply it.  We were rather naive a little  bit but it seemed like fun, and that George, having been overseas and in the  war, I think he wanted to get back to something natural.  I know he did not want to go . . .<br/>
<br/>GB:      Do you remember the definition of the  farmer?<br/>
<br/>BB:      A man not standing in his field.  That’s funny.   But anyway, he wanted to do something other than ____ [1:26].  He wanted something you could touch or . . .<br/>
<br/>GB:      Different than what my father had  successfully done and my uncle who was a big ____[1:41] from Austria.  Didn’t want to do that.  I did not know I did want to do but I did not  want to do that.<br/>
<br/>JM:        So you ultimately decided on the oil  industry?<br/>
<br/>GB:      Well, we were offered a job at Dresser  Industries.  A friend of my father’s,  Neil Ballen (sp?), one of our closest friends in life, and he said, “You ought  to not go in the east, don’t stay in the east.   You ought to go to Texas.  You ought to go to Odessa, Texas.”  We said, “Where is that?”  We did not know where Odessa was and down we went.  The rest is history.  We loved it ever since.  We loved West Texas where we lived for 12  years or so and then, of course, we loved Houston.<br/>
<br/>JM:        You started on the ground floor –  salesman, sweeping the shop floor.  You  had a little stint in California.<br/>
<br/>GB:      That was for like a training program for  selling lot bits in Bakersfield  and helping build pumps.  I remember the  steelworker’s union in Huntington Park,   California at _____[2:46].  So we did different jobs at different Dresser  companies and learned every step of the way.   We had a great diverse experience in where we lived.<br/>
<br/>JM:        Eventually moved back to West Texas, had a successful company there.  When did the seed get planted that you would  come to Houston  to launch Zapata Offshore?<br/>
<br/>GB:      We had already launched Zapata Offshore  but to run an offshore company, you really need to be in Houston for something like that.<br/>
<br/>BB:      How far offshore can you get?<br/>
<br/>GB:      So, we were in Midland.   That did not seem like a good place to run the company from so I was  CEO, you might say, of the company and down we moved, and have been very happy  ever since.<br/>
<br/>JM:        You knew of Houston then by its reputation as kind of a  mecca center for . . .<br/>
<br/>GB:      It was.   Tulsa claimed to be the oil capital of  the world but then they have been eclipsed by Houston.<br/>
<br/>JM:        You had known in Midland  and West Texas, you kind of had the idyllic .  . . you talked about it . . .<br/>
<br/>BB:      He loved Midland  but, having said that, the minute we got to Houston,  we loved Houston.  Maybe we are smarter than some people but Houston is a great  city.  It is a city that you can bring up  your children, diversity . . . I mean, wonderful hospitals, universities.  It is just a wonderful place.  Great businesses.  And we had friends there that moved there  from Midland, moved to Houston.<br/>
<br/>JM:        About the same time?<br/>
<br/>BB:      Well, they came a little earlier.<br/>
<br/>GB:      Some came a little earlier.<br/>
<br/>BB:      Some later but Hugh Ligby (sp?), they came  earlier.  They went to Pennsylvania  and then came back to Houston  for Pennzoil.<br/>
<br/>GB:      I am not sure if Hugh went to Houston before he went  over there.<br/>
<br/>BB:      No, I think he went from Midland to Pennzoil.<br/>
<br/>GB:      That’s right. <br/>
<br/>BB:      But Fred Chambers . . . we met a lot of  very close friends.<br/>
<br/>JM:        All the way from West Texas to Houston?<br/>
<br/>BB:      We knew Bob Mosbacher, didn’t we, after we  moved?<br/>
<br/>GB:      Yes.<br/>
<br/>BB:      But, you know, I am one who believes the  friends you make in the first 6 grades are the friends for life.  They are the friends for life if you are  compatible but we made more best friends in Houston, Texas,  more generous people and more wonderful friends.<br/>
<br/>GB:      That’s right.<br/>
<br/>
</p>
<p><a href="javascript:flaSeekCue('1');"><img src="https://web.archive.org/web/20131114224918im_/http://digital.houstonlibrary.org/oral-history/media/watch2.gif" alt="cue point" width="21" height="25" border="0" align="absmiddle"/></a></p>
<p>JM:        And you arrive with a young family, growing  family . . .<br/>
<br/>
GB:      Growing was the right word.<br/>
<br/>
BB:      We had 4 boys when we arrived and Houston was good to us –  they gave us God’s gift of the _____[5:37].<br/>
<br/>
GB:      How many were born in Houston?<br/>
<br/>
BB:      One.<br/>
<br/>
GB:      Just Dorothy.<br/>
<br/>
JM:        Just Dorothy?  Shortly after you arrived there.  Jumping from family and business to politics,  1961 was a fairly momentous year for Republicans in Texas.   You had John   Tower, who probably  picked up the Senate seat.  I am  wondering . . . around that time, you made a decision to run for Harris County  Chairman and the Harris County of the 1960s was not the Harris County  of the 1990s where Republicans were dominant . . . what led you to enter in on  that . . .<br/>
<br/>
GB:      Well, I forget how it came about but the  Party was not a tiny party and had always been interested in politics but  somebody came to see me and said, “You ought to run for County Chairman.”  In those days, we had the Birch Society, the  real Right-Wing nasty people and so, I took them on.  It was interesting and I am glad I did it.<br/>
<br/>
BB:      What we didn’t know was in those days and  maybe still, Harris   County had something like  289 precincts, so it was goodbye to George Bush.  He went every night to precincts and he  really won . . .<br/>
<br/>
GB:      Grass roots.<br/>
<br/>
BB:      That’s right, and made a lot of friends  that way.  He would always say, “My wife  is the one sitting in the back doing knitting.”   Of course, I was doing needlepoint but anyway . . .<br/>
<br/>
JM:        So you hit all 289 precincts or many of  them?<br/>
<br/>
GB:      Almost.<br/>
<br/>
BB:      I think you did.<br/>
<br/>
GB:      And from there . . . of course, your dad  had famously run for the Senate in Connecticut  after serving as town moderator, and you made a similar jump from Harris County  Chairman to running for the Senate in 1964.   What led you to make that . . .<br/>
<br/>
JM:        Well, Peter O’Donnell was the State Republican  Chairman.  I think they were desperate to  get somebody to run and we made the case that helped to get the money which was  a lot but nothing like it is today, and so I was intrigued by it, got in and  gave it my all.  Bard (sp??) did the  same.  We got killed.  We got beat by 57-43, maybe.  Maybe not quite 43.  But anyway, we got beat badly but I got the  bug and decided to try again a couple of years later for the Congress.<br/>
<br/>
JM:        The 1964 election, was that liquor by  the drink or was that 1970?<br/>
<br/>
GB:      1970.   That is Lloyd Bentsen.<br/>
<br/>
BB:      That was Barry Goldwater against Lyndon  Johnson which was . . .<br/>
<br/>
GB:      That was 1964.  I got more votes than Goldwater did in the  state but was killed nevertheless.<br/>
<br/>
BB:      For some reason, Goldwater would go to Fort Worth or someplace  and speak up against the airplane factory that was there.  I remember he went to Florida and he spoke against any help for  older people.  I mean, his timing was a  little off.  He was a wonderful man.<br/>
<br/>
GB:      _______[8:53].<br/>
<br/>
JM:        In 1966, you did throw your hat in to  Congressional District 7.<br/>
<br/>
GB:      Against Frank Briscoe.  He was a very popular district attorney at  that point.  They never had a Republican  elected in the 7th  District  of Texas.  We worked hard.  We had a lot of grass roots people out in the  neighborhoods passing out leaflets and knocking on doors.  So I think we won a pretty good victory then.<br/>
<br/>
BB:      It is hard to believe but some of those  same volunteers are at the Office of Volunteers in Houston now.<br/>
<br/>
GB:      As we speak.<br/>
<br/>
BB:      As we speak.  That in itself would make running for office  great.<br/>
<br/>
JM:        So, 1966 was the dawn of the Bush Belles  or did you have them in 1964, too?<br/>
<br/>
GB:      I think we had them in 1964, yes.<br/>
<br/>
JM:        O.K., and, as you say, many of them are  still with you.<br/>
<br/>
GB:      Still ringing.<br/>
<br/>
JM:        1967 was an interesting time in your  political career.  You had the Open  Housing Bill that came down in April.   You had the King assassination, I think, the week before.  That was kind of a turbulent time not only in  Houston but  across the country, and you cast a hard vote and came back to face the  constituents afterwards and managed to turn them around.  Talk about that, that period of time there.<br/>
<br/>
GB:      Do you remember the details?  I do not remember the details.<br/>
<br/>
BB:      Well, you flew down and went to a high  school, I think.<br/>
<br/>
JM:        In Memorial?<br/>
<br/>
BB:      In Memorial, to a very angry group.<br/>
<br/>
GB:      Angry constituents.<br/>
<br/>
BB:      That’s right, because they really thought  that your voting for open housing was terrible and you felt that people who  serve a country . . .<br/>
<br/>
GB:      Yes, the veterans that came back could not  get any place to live.  But I think it  turned out to not be that momentous of an occasion but I just think we had to  take charge there and we did, and people supported me at the meeting actually.<br/>
<br/>
BB:      That’s right.  They cheered when he left.<br/>
<br/>
</p>
<p><a href="javascript:flaSeekCue('2');"><img src="https://web.archive.org/web/20131114224918im_/http://digital.houstonlibrary.org/oral-history/media/watch2.gif" alt="cue point" width="21" height="25" border="0" align="absmiddle"/></a></p>
<p>JM:        They jeered when you walked in and  cheered when you left.<br/>
<br/>
GB:      Maybe they were glad to see me go.<br/>
<br/>
BB:      No, that’s not right.<br/>
<br/>
JM:        Were there any other issues that stand  out from your days in the House?  I know  you enjoyed serving under President Ford’s minority leadership.  Anything else that stands out?<br/>
<br/>
GB:      Being a freshman member, the first one in  history, I think, of the Ways and Means Committee, working under the leadership  of Wilbur Mills, for whom I had great respect.   Wilbur Mills got in trouble because of Fanne Fox in the Tidal  Basin.  He was a great man, and I learned  from him. One thing I learned from him is to show respect to everybody on your  committee or, in my case, everybody in your government.  Give them time.  He did that.   He would wait and everybody else would leave and I had my chance to  question Walter Reuther or something like that – all because of the courtesy of  Wilbur Mills.  He knew more about tax law  than anybody else, so I learned a lot from him, and I ended up feeling very, very  close to him. <br/>
<br/>
JM:        And, of course, in the 1960s, it was a  little bit different than it is these days.   It seems most House members get an apartment and keep their house in  their district or where they live.  You  got the house but you were still commuting back and forth.  What was that like?<br/>
<br/>
BB:      I think the government paid them for 2 or  maybe 4 trips back to Houston.  George  went every weekend.  But it was worth  it.  He was unopposed when he ran again  which is amazing and the children were dragged around Washington going  sightseeing.  I remember standing outside  the White House watching Nixon’s church light up and the children, their little  faces.  I never thought we would live in  the White House but we did that.<br/>
<br/>
JM:        Then, 1970 came along and you made a famous  pilgrimage to the LBJ Ranch and you went to go visit with the former president  and seek his advice.  Talk about that  encounter.<br/>
<br/>
GB:      Well, I thought we were going to be  running against Ralph Yarborough, and he gave me good advice.  He did not like Yarborough.  I think he would have been happy to see me  win.  He could not and would not endorse  me.  But then, Lloyd Bentsen got in the  race.  Lloyd was a very attractive, nice,  experienced person.  I still thought I  would win Barbara Jordan who was very popular in Houston, and “If Bentsen wins  the primary against Yarborough, you can go to the Senate.  Pick out your seat.  You will be in.  Everybody that was on the other side will be  for you.”  Well, that never materialized  either.  Bentsen did a great job in  bringing and keeping people together.<br/>
<br/>
BB:      And liquor by the drink.<br/>
<br/>
GB:      Well, then, they had this liquor by the  drink in East Texas.  It was kind of a mandate  on the ballot.  And huge flocks of  people, Baptists and others, came to the polls and they were not our  constituency.  In those days, a  Republican did not do well in East Texas.   They just did not carry East Texas, did not make a showing in East  Texas.  It was Dallas, Houston, West  Texas and, to some degree, San Antonio.   That really killed me when they had that liquor by the drink stuff, but  Bentsen was a good campaigner and a thoroughly decent person.<br/>
<br/>
JM:        The 1970 campaign brought you together  with 2 other Houstonians that would be by your side during your ascent – Jim  Baker and Bob Mosbacher. Secretary Baker served as your Harris County Chairman  and I think Secretary Mosbacher helped you on the fundraising and other  matters.  Talk about that partnership as  it grew out.<br/>
<br/>
GB:      Well, just from that, from those early  political days, we became the fastest of friends and both served in the  Administration – Baker as Secretary of State and Mosbacher as Secretary of  Commerce.  Both did outstanding jobs and  both were experienced.  Baker was one of  the most experienced lawyers in Houston and Mosbacher, a highly successful oil  guy. So, I was very lucky not only for their friendship but for having them at  my side in those political battles.<br/>
<br/>
JM:        O.K.   After the 1970 campaign, you received a series of high-level  appointments that took you to a number of different cities – New York,  Washington, China.  Did you keep in touch  with friends in Houston?  Did you make it  back to the town often?<br/>
<br/>
BB:      As much as we possibly could.<br/>
<br/>
GB:      Oh, yes, we stayed in touch.  Definitely.<br/>
<br/>
BB:      And our Houston friends came to  Washington, of course.<br/>
<br/>
GB:      We kept our house then.<br/>
<br/>
BB:      No, we kept the . . . <br/>
<br/>
GB:      We had a house over on Longmont.<br/>
<br/>
BB:      Jack Fitch sold us that property and then  we stayed at the Houstonian, I think.  We  got ridiculed for that – both for the property which they said was small and we  would never build on, and we built on; and the Houstonian was a really great  place to stay.  A lot of exercising, and  the location was good.  I hated that we  sold our house on Longmont.  Oh, Longmont  we did during the Congress _____[16:29].   We had that _____.<br/>
<br/>
GB:      It was very nice.<br/>
<br/>
BB:      It was very nice.  We loved it.   We came home for Christmas.  All  the vacations we took.<br/>
<br/>
</p>
<p><a href="javascript:flaSeekCue('3');"><img src="https://web.archive.org/web/20131114224918im_/http://digital.houstonlibrary.org/oral-history/media/watch2.gif" alt="cue point" width="21" height="25" border="0" align="absmiddle"/></a></p>
<p>JM:        In 1977, after the inauguration of our  39th president . . .<br/>
<br/>
BB:      Carter?<br/>
<br/>
JM:        President Carter.  You came back to Houston and you dove back  into life there and you were an Adjunct Professor at Rice University?  Is that true?<br/>
<br/>
GB:      True.   I did not teach many classes there but I enjoyed it very much.  I asked what “adjunct” meant and they said it  means you don’t get paid, and some thought I was being overly compensated but  nevertheless, I learned a lot.  Rice is a  great institution.  It is right there in  Houston.<br/>
<br/>
JM:        It wasn’t too long after you got back .  . . I read in Secretary Baker’s book that it was right after his run for Attorney  General of Texas that you gave him a call and said, “Let’s get going.”  And you got T’d up in 1980 and you had an  office, I believe – I think you pointed it out to me at one point, it was right  off the West Loop there right up on 610.<br/>
<br/>
BB:      That’s right.<br/>
<br/>
GB:      Right.<br/>
<br/>
JM:        And you started off as an asterisk  famously.  There was another Texan  actually, Governor Connally, who had an office in Houston and whatnot, and, as  Secretary Mosbacher tells it, a lot of people came up to him and said, “Well,  Connally is the star.”<br/>
<br/>
GB:      “It is his turn.”  I remember one of the great families there  said . . .<br/>
<br/>
BB:      She was a Democrat who became a  Republican.<br/>
<br/>
GB:      That’s all right, I mean, you need that,  but they said, “It is his turn.”  Well, I  didn’t look at it like anybody gets a turn to run for president.<br/>
<br/>
BB:      Well, and Nixon liked him very, very much.<br/>
<br/>
GB:      Oh, very high.  Nixon was very, very high . . .<br/>
<br/>
BB:      He really wanted him to go for president.<br/>
<br/>
JM:        Also contemplated putting him on the  ticket after Agnew.<br/>
<br/>
GB:      He definitely wanted to be president.  Nixon, in my view, wanted Connally to be  president.<br/>
<br/>
JM:        So you started off, gained traction, got  the name out there, mounted a serious challenge against Governor Reagan but at  the end, the delegate count went his way.   You came back to Houston and I think that is when you bought your lot  from Mr. Fitch.<br/>
<br/>
BB:      No, we had that for years.<br/>
<br/>
JM:        You had that already?  O.K.<br/>
<br/>
BB:      That is when the people stood on the lot  and said, “They’ll never build here.”  We  had it for about 16 years.  It is pretty  tiny.<br/>
<br/>
JM:        And that is when you made plans I think  to finally construct but then fate interceded once again.  I guess before you went to Detroit, you  started with some plans to build and then in Detroit, fate interceded once again  and you are on the ticket.<br/>
<br/>
BB:      You know, we should go back a little  because, really, we bought this house knowing George was out of office when he  lost to Reagan the presidential.  So, we  sold the grey house across the way and bought this, thinking he would go out  and work and make money.  Well, as it  turned out, we were lucky.  We sold our  house in Houston that we loved, the grey house, so we could afford this.  Wasn’t that the way it sort of went?<br/>
<br/>
GB:      I think so.  I am a little vague on details.<br/>
<br/>
BB:      Yes, I think that happened.  And then, after . .  . lets see, who was elected?  It must been Clinton . . . that we came down  and built on our lot. We built our house in _______[20:14] and it was up in no  time at all.  It was a wonderful  house.  Very happy in that wonderful neighborhood.<br/>
<br/>
</p>
<p><a href="javascript:flaSeekCue('4');"><img src="https://web.archive.org/web/20131114224918im_/http://digital.houstonlibrary.org/oral-history/media/watch2.gif" alt="cue point" width="21" height="25" border="0" align="absmiddle"/></a></p>

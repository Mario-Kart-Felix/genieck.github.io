<br/>
GB:      Oh, yes.   And a lot of the same people that helped me in that office, volunteered,  still help me now in our Houston office as volunteers.  It is amazing.  Just wonderful people.<br/>
<br/>
BB:      They were there in the 1960s, too.<br/>
<br/>
GB:      Never got a dime.  Just dedicated.  Never properly said thank you.<br/>
<br/>
JM:        You would obviously make frequent visits  back as vice-president.<br/>
<br/>
GB:      Oh, yes.<br/>
<br/>
JM:        And then, as president, you, on 2  occasions, chose to really bring the world to Houston.  You brought the ____[21:15] summit.<br/>
<br/>
GB:      That was a big one.  Bloody hot, but we brought them.<br/>
<br/>
JM:        July in Houston!<br/>
<br/>
GB:      Micherant (sp?), Thatcher.  I don’t think the Soviets were there.  Maybe they were. <br/>
<br/>
BB:      Canada.<br/>
<br/>

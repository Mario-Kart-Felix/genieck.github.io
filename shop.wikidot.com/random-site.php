<p>Welkom!</p>
<p>Wij zijn studenten in de Toegepaste Psychologie aan de Katholieke Hogeschool in Kortrijk, departement Ipsoc.<br />
Voor het vak Sociaal-Agogische Vaardigheden kregen wij het thema hoogbegaafdheid.<br />
Wij trachten dus onderzoek te voeren naar alles omtrent hoogbegaafdheid in naam van het expertisecentrum hoogbegaafdheid aan Katho.<br />
Via deze wiki proberen wij het expertisecentrum bekend te maken aan externen.</p>
<p>Een introducerende video over mogelijke "problemen" van hoogbegaafde kinderen:<br />
Klik <a href="http://www.youtube.com/watch?v=Xy0VYuoIvT0">hier</a></p>
<p>Momenteel vertoeven wij nog in de eerste fase van ons project. Eerst hebben we nagedacht over enkele doelstellingen die te maken hebben met ons project. Dit zijn onze (voorlopige) doelstellingen:<br />
- Een logo voor het expertisecentrum<br />
- Een naam voor het expertisecentrum<br />
- Een website over het expertisecentrum<br />
Onze einddoelstelling is om uiteindelijk een studiedag te houden rond hoogbegaafdheid en het expertisecentrum.</p>
<p>Vervolgens dachten wij na over hoe we dit allemaal zouden realiseren, er moet geld in het laatje komen om deze studiedag te organiseren. We hebben hierdoor beslist om een wafel- en cakeverkoop te houden in de departementen van KATHO<br />
Binnenkort is het tijd voor het echte werk, we zullen ons project presenteren aan andere SOVA-groepen. Hiervoor moeten we ook wat over hoogbegaafdheid kunnen vertellen. We verdiepten ons reeds in de theoretische kant van het onderwerp.<br />
Onze eerste presentatie vindt plaats in februari, daarna kunnen we ons weer concentreren op onze andere doelstellingen.</p>
<p>Deze wiki is ontstaan door medewerking van SOVA-groep Ac (zie groepsleden).</p>


<p>Although the GameCube supports simultaneous play with up to four players, <em>Animal Crossing </em>only allows one player at a time. The game can store up to four player profiles in one shared town, and human players can interact with friends or family members who play the game, but only indirectly, by leaving notes or gifts, completing tasks, or even planting flowers or trees. Furthermore, <em>Animal Crossing </em>binds the game world to the real world, synchronizing its date and time to the console clock. Time passes in real time in <em>Animal Crossing</em>&mdash;it gets dark at night, snows in the winter, and the animals go trick-or-treating on Halloween. Since game time is linked to real time, a player can conceptualize the game as a part of his daily life rather than a split out of it.</p>
<p><img style="vertical-align: middle; margin: 5px auto; border: 1px solid black; display: block;" src="/db_area/images/feature/199599/animalcrossinganimals.jpg" alt="" width="560" height="286" /></p>
<p>This binding of the real world to the game world creates opportunities for families or friends to collaborate in a way that might be impossible in a simultaneous multiplayer game. Since the whole family shares a single GameCube, the game&rsquo;s persistent state facilitates natural collaboration between family members with different schedules. For example, a child might find a fossil during the afternoon, then mail it to her father&rsquo;s character in the game. At bedtime, she could let Dad know that she needs to have it analyzed at the central museum so she can take it to the local gallery the next day. As critics Kurt Squire and Henry Jenkins wrote of the game, &ldquo;Families (of all types) live increasingly disjointed lives, but the whole family can play <em>Animal Crossing </em>even if they can rarely all sit down to dinner together.&rdquo;</p>
<p>One of the most challenging projects in the game is paying off the mortgage on one&rsquo;s house. <em>Animal Crossing </em>allows players to upgrade their homes, but doing so requires paying off a large note the player must take out to start the game in the first place. Then the player must pay renovation mortgages for even larger sums. While the game mercifully omits some of the more punitive intricacies of long-term debt, such as compounding interest, improving one&rsquo;s home does require consistent work in the game world. Catching fish, hunting for fossils, finding insects, and doing jobs for other townsfolk all produce income that can be used to pay off mortgage debt&mdash;or to buy carpets, furniture, and objects to decorate one&rsquo;s house.</p>
<p><em>Animal Crossing </em>deploys a procedural rhetoric about the repetition of mundane work as a consequence of contemporary material property ideals. When my (then) five-year-old began playing the game seriously, he quickly recognized the dilemma he faced. On the one hand, he wanted to spend the money he had earned from collecting fruit and bugs on new furniture, carpets, and shirts. On the other hand, he wanted to pay off his house so he could get a bigger one like mine. Then, once he did amass enough savings to pay off his mortgage, the local shopkeeper and real estate tycoon Tom Nook offered to expand his house. While it is possible to refrain from upgrading, Nook, an unassuming raccoon, continues to offer renovations as frequently as the player visits his store. My son began to realize the trap he was in: the more material possessions he took on, the more space he needed, and the more debt he had to take on to provide that space. And the additional space just fueled more material acquisitions, continuing the cycle.</p>


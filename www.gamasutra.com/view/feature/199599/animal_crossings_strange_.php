                <p><em><strong>While some are learning about the peculiar pleasure of </strong></em><strong>Animal Crossing</strong><em><strong> thanks to the series&rsquo; latest release on Nintendo 3DS, the game has long charmed and puzzled players and critics. In this excerpt from his <a href="http://www.amazon.com/Persuasive-Games-Expressive-Power-Videogames/dp/0262514885">2007 book Persuasive Games</a>, Ian Bogost discusses the game&rsquo;s first edition (released for the GameCube in 2002) in relation to his theory of &ldquo;procedural rhetoric,&rdquo; the act of expressing ideas through rules. While some details have changed over the years, </strong></em><strong>Animal Crossing</strong><em><strong>&rsquo;s overall themes remain constant: the strange, unresolved conflict between consumption and naturalism.</strong></em></p>
<p>The Nintendo GameCube videogame <em>Animal Crossing </em>is an &ldquo;animal village simulator.&rdquo; Players move into a town filled with cartoonish animal characters and buy a house, then work, trade, and personalize their microenvionment. The game offers a series of innocuous, even mundane activities like bug catching, gardening, and wallpaper designing; like <em>The Sims</em>, <em>Animal Crossing</em>&rsquo;s primary metaphors are social interaction and household customization.</p>
                <div class="hide-phone">
                    <div class="module_right right bottom30">
                                                                                    <!-- Beginning Sync AdSlot 6 for Ad unit Gamasutra/ ### size: [[300,250]]  -->
